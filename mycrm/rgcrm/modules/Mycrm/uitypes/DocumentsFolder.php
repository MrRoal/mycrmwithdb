class Mycrm_DocumentsFolder_UIType extends Mycrm_Base_UIType {

	/**
	 * Function to get the Template name for the current UI Type object
	 * @return <String> - Template Name
	 */
	public function getTemplateName() {
		return 'uitypes/DocumentsFolder.tpl';
	}

	/**
	 * Function to get the Display Value, for the current field type with given DB Insert Value
	 * @param <Object> $value
	 * @return <Object>
	 */
	public function getDisplayValue($value) {
		$db = PearDatabase::getInstance();
		$result = $db->pquery('SELECT * FROM mycrm_attachmentsfolder WHERE folderid = ?', array($value));
		if($db->num_rows($result)) {
			return $db->query_result($result, 0, 'foldername');
		}
		return false;
	}
}